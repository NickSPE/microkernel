            if success:
                print(f"‚úÖ Archivo '{filename}' creado exitosamente")
                info = self.fs_service.get_file_info(filename)
                if info:
                    print(f"üóÇÔ∏è Virtual: fs_service.root_dir.files['{filename}']")
                    print(f"üíæ REAL: {info['real_path']}")
                    print(f"üìÅ En disco: {'‚úÖ S√ç' if info['exists_on_disk'] else '‚ùå NO'}")
