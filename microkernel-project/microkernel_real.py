        while True:
            try:
                command_input = input("\nüéÆ real-microkernel> ").strip()
                
                if not command_input:
                    continue
                    
                parts = command_input.split()
                command = parts[0].lower()
                args = parts[1:]
                
                if command == "help":
                    self._print_help()
                elif command == "status":
                    self._handle_status(args)
                elif command == "fail":
                    self._handle_fail(args)
                elif command == "recover":
                    self._handle_recover(args)
                elif command == "test":
                    self._handle_test(args)
                elif command == "use":
                    self._handle_use(args)
                elif command == "inspect":
                    self._handle_inspect(args)
                elif command == "real":
                    self._handle_real(args)
                elif command == "demo":
                    self._handle_demo()
                elif command == "exit":
                    print("üëã ¬°Hasta luego!")
                    print(f"üíæ Tus archivos quedan guardados en: {self.fs_service.real_base_path}")
                    break
                else:
                    print(f"‚ùå Comando '{command}' no reconocido. Escribe 'help' para ver los comandos disponibles.")
                    
            except KeyboardInterrupt:
                print("\nüëã ¬°Hasta luego!")
                print(f"üíæ Tus archivos quedan guardados en: {self.fs_service.real_base_path}")
                break
            except Exception as e:
                print(f"üí• Error: {e}")

